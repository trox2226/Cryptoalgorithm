// 密钥移位表
const uint8_t key_shifts[16] = {
    1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1
};

//初始置换
uint64_t initial_permutation(uint64_t input) {
    uint64_t output = 0;
    for (int i = 0; i < 64; i++) {
        int bit_pos = IP[i] - 1;
        int bit = (input >> (63 - bit_pos)) & 1;
        output |= (uint64_t)bit << (63 - i);
    }
    return output;
}
//逆初始置换
uint64_t inverse_initial_permutation(uint64_t input) {
    uint64_t output = 0;
    for (int i = 0; i < 64; i++) {
        int bit_pos = IP_INV[i] - 1;
        int bit = (input >> (63 - bit_pos)) & 1;
        output |= (uint64_t)bit << (63 - i);
    }
    return output;
}
//生成子密钥
void generate_subkeys(const unsigned char* key, DES_KEY* des_key) {
    uint64_t key_block = 0;
    for (int i = 0; i < 8; i++) {
        key_block = (key_block << 8) | key[i];
    }

    uint64_t permuted_choicel = 0;
    for (int i = 0; i < 56; i++) {
        int bit = (key_block >> (64 - PC_1[i])) & 1;
        permuted_choicel |= ((uint64_t)bit << (55 - i));
    }

    uint32_t c = (permuted_choicel >> 28) & 0x0FFFFFFF;
    uint32_t d = permuted_choicel & 0x0FFFFFFF;
    for (int i = 0; i < 16; i++) {
        int shift = key_shifts[i];
        c = ((c << shift) | (c >> (28 - shift))) & 0x0FFFFFFF;
        d = ((d << shift) | (d >> (28 - shift))) & 0x0FFFFFFF;

        uint64_t cd = (c << 28) | d;
        uint64_t subkey = 0;
        for (int j = 0; j < 48; j++) {
            int bit = (cd >> (56 - PC_2[j])) & 1;
            subkey |= ((uint64_t)bit << (47 - j));
        }

        des_key->subkeys[i] = subkey;
    }
}

//DES加密块
void des_encrypt_block(unsigned char* block, const DES_KEY* key) {
    uint64_t data_block = 0;
    for (int i = 0; i < 8; i++) {
        data_block |= (uint64_t)block[i] << (8 * (7 - i));
    }

    data_block = initial_permutation(data_block);
    uint32_t left = data_block >> 32;
    uint32_t right = data_block & 0xFFFFFFFF;

    for (int i = 0; i < 16; i++) {
        uint32_t temp = left;
        uint64_t expanded = 0;
        for (int j = 0; j < 48; j++) {
            int bit_pos = E[j] - 1;
            int bit = (right >> (31 - bit_pos)) & 1;
            expanded |= ((uint64_t)bit << (47 - j));
        }

        expanded ^= key->subkeys[i];
        uint32_t substituted = 0;
        for (int j = 0; j < 8; j++) {
            int row = ((expanded >> (47 - j * 6)) & 1) << 1 | ((expanded >> (42 - j * 6)) & 1);
            int col = (expanded >> (44 - j * 6)) & 0xF;
            substituted |= ((SBOX[j][row * 16 + col] & 0xF) << (28 - j * 4));
        }

        uint32_t permuted = 0;
        for (int j = 0; j < 32; j++) {
            int bit = (substituted >> (32 - P[j])) & 1;
            permuted |= (bit << (31 - j));
        }
        left = right;
        right = temp ^ permuted;
    }
    data_block = ((uint64_t)right << 32) | left;
    data_block = inverse_initial_permutation(data_block);

    for (int i = 0; i < 8; i++) {
        block[i] = (data_block >> (8 * (7 - i))) & 0xFF;
    }
}

// DES解密块
void des_decrypt_block(unsigned char* block, const DES_KEY* key) {
    uint64_t data_block = 0;
    for (int i = 0; i < 8; i++) {
        data_block |= (uint64_t)block[i] << (8 * (7 - i));
    }

    data_block = initial_permutation(data_block);
    uint32_t left = data_block >> 32;
    uint32_t right = data_block & 0xFFFFFFFF;

    for (int i = 15; i >= 0; i--) {
        uint32_t temp = left;
        uint64_t expanded = 0;
        for (int j = 0; j < 48; j++) {
            int bit_pos = E[j] - 1;
            int bit = (right >> (31 - bit_pos)) & 1;
            expanded |= ((uint64_t)bit << (47 - j));
        }

        expanded ^= key->subkeys[i];
        uint32_t substituted = 0;
        for (int j = 0; j < 8; j++) {
            int row = ((expanded >> (47 - j * 6)) & 1) << 1 | ((expanded >> (42 - j * 6)) & 1);
            int col = (expanded >> (44 - j * 6)) & 0xF;
            substituted |= ((SBOX[j][row * 16 + col] & 0xF) << (28 - j * 4));
        }

        uint32_t permuted = 0;
        for (int j = 0; j < 32; j++) {
            int bit = (substituted >> (32 - P[j])) & 1;
            permuted |= (bit << (31 - j));
        }

        left = right;
        right = temp ^ permuted;
    }

    data_block = ((uint64_t)right << 32) | left;
    data_block = inverse_initial_permutation(data_block);

    for (int i = 0; i < 8; i++) {
        block[i] = (data_block >> (8 * (7 - i))) & 0xFF;
    }
}

// 三重DES加密块函数
void triple_des_encrypt_block(unsigned char* block, const TRIPLE_DES_KEY* key) {
    des_encrypt_block(block, &key->key1);
    des_decrypt_block(block, &key->key2);
    des_encrypt_block(block, &key->key3);
}

// 三重DES解密块函数
void triple_des_decrypt_block(unsigned char* block, const TRIPLE_DES_KEY* key) {
    des_decrypt_block(block, &key->key3);
    des_encrypt_block(block, &key->key2);
    des_decrypt_block(block, &key->key1);
}